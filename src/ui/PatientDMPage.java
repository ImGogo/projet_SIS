    public final void initTable(){
        JFrame popup = new PopupLoading();
        
        SwingWorker sw = new SwingWorker(){
            @Override
            protected String doInBackground() throws Exception 
            {
                String headers[] = {"Date", "Type", "Service", "N° de séjour", "ID"};
                DefaultTableModel model = new DefaultTableModel(null, headers);
                popup.setVisible(true);
                
                try{
                    for(Visite c : ConnectBD.getListeConsultationFromIpp(PatientDMPage.this.patient.getIpp())){
                        model.addRow(c.getConsultationForConsultationListe());
                    }
                    table.setModel(model);
                } catch (Exception e){
                    System.err.println(e.getMessage());
                    PopupFactory.createPopupErreurConnexion();
                }
                return "";
            }
  
            @Override
            protected void done() 
            {
                
                popup.setVisible(false);
            }
        };
          
        // executes the swingworker on worker thread
        sw.execute(); 
    }
    
    public final void setTableLook(){
        
        table.setBorder(null);
        DefaultTableCellRenderer head_render = new DefaultTableCellRenderer(); 
        
        head_render.setBackground(new Color(255,255,255));
        head_render.setForeground(new Color(31,58,105));
        table.getTableHeader().setDefaultRenderer(head_render);
        table.setFillsViewportHeight(true);
        table.setDefaultEditor(Object.class, null);
        
        table.setDefaultRenderer(Object.class, new TableCellRenderer(){
            private DefaultTableCellRenderer DEFAULT_RENDERER =  new DefaultTableCellRenderer();
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                Component c = DEFAULT_RENDERER.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                if(isSelected){
                    c.setBackground(new Color(31, 58, 105));
                }else{
                    if (row%2 == 0){
                        c.setBackground(Color.WHITE);
                    }
                    else {
                        c.setBackground(new Color(230, 230, 240));
                    }     
                }
                return c;
            }
        });
    }
    
