    public final void initLabels(){
        
        
        this.lblPraticien.setText( personnel.toString() );
        this.lblService.setText( personnel.getService().toString() );
        this.nameLb.setText( personnel.toString() );
        this.serviceLb.setText( personnel.getService().toString() );
        
        try {
            this.visite = ConnectBD.getVisiteByIdDm(idDM);
            this.lblTypeVisite.setText( visite.getType() );
            this.portalLb.setText( visite.getType().toUpperCase() );
            fc.Date d = visite.getDateEntree();
            this.lblDate.setText( d.toString() );
            this.lblHeure.setText( d.getHeureMinute() );
            this.lblMotif.setText( visite.getMotif() );
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }
    
    public void addPrescription(Prescription p){
        visite.addPrescription(p);
        updatePrescriptionTable();
    }
    
    public void addPrestation(Prestation p) {
        visite.addPrestation(p);
        updatePrestationTable();
    }
    
    public void updatePrescriptionTable(){
        String headers[] = {"Medicament", "Dosage", "Posologie", "Debut", "Fin"};
        DefaultTableModel model = new DefaultTableModel(null, headers);
        
        for(Prescription c : visite.getPrescriptions()){
            model.addRow(c.getPrescriptionForTable());
        }
        this.prescriptionTable.setModel(model);
    }
    
    public void updatePrestationTable(){
        String headers[] = {"Prestation", "Service"};
        DefaultTableModel model = new DefaultTableModel(null, headers);
        
        for(Prestation c : visite.getPrestations()){
            model.addRow(c.getPrestationForTable());
        }
        this.prestationTable.setModel(model);
    }
